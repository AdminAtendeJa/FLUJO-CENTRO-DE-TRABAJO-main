CREATE TABLE IF NOT EXISTS public.tramites_categorias (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  codigo text NOT NULL UNIQUE,
  nombre text NOT NULL,
  descripcion text,
  color text,
  orden integer NOT NULL DEFAULT 0,
  activo boolean NOT NULL DEFAULT true,
  creado_en timestamptz NOT NULL DEFAULT now(),
  actualizado_en timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS public.tramites_catalogo (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  codigo text NOT NULL UNIQUE,
  nombre text NOT NULL,
  descripcion text,
  categoria_id bigint REFERENCES public.tramites_categorias(id) ON DELETE SET NULL,
  precio_base numeric(12,2) NOT NULL DEFAULT 0,
  costo_base numeric(12,2) NOT NULL DEFAULT 0,
  margen_objetivo numeric(8,4) NOT NULL DEFAULT 0,
  moneda text NOT NULL DEFAULT 'BRL',
  color text,
  orden integer NOT NULL DEFAULT 0,
  activo boolean NOT NULL DEFAULT true,
  creado_en timestamptz NOT NULL DEFAULT now(),
  actualizado_en timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT tramites_catalogo_precio_chk CHECK (precio_base >= 0),
  CONSTRAINT tramites_catalogo_costo_chk CHECK (costo_base >= 0)
);

CREATE TABLE IF NOT EXISTS public.tramites_precios (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  tramite_id bigint NOT NULL REFERENCES public.tramites_catalogo(id) ON DELETE CASCADE,
  precio numeric(12,2) NOT NULL,
  costo numeric(12,2) NOT NULL DEFAULT 0,
  moneda text NOT NULL DEFAULT 'BRL',
  vigente_desde date NOT NULL DEFAULT CURRENT_DATE,
  vigente_hasta date,
  activo boolean NOT NULL DEFAULT true,
  observaciones text,
  creado_en timestamptz NOT NULL DEFAULT now(),
  actualizado_en timestamptz NOT NULL DEFAULT now(),
  CONSTRAINT tramites_precios_fechas_chk CHECK (vigente_hasta IS NULL OR vigente_hasta >= vigente_desde),
  CONSTRAINT tramites_precios_precio_chk CHECK (precio >= 0),
  CONSTRAINT tramites_precios_costo_chk CHECK (costo >= 0)
);

