--    Distribución y rentabilidad por trámite
-- ============================================================

DROP VIEW IF EXISTS public.v_dashboard_servicios CASCADE;

CREATE OR REPLACE VIEW public.v_dashboard_servicios
WITH (security_invoker = true) AS
SELECT
  COALESCE(tc.codigo, upper(trim(e.servicio))) AS codigo_servicio,
  COALESCE(tc.nombre, e.servicio, 'Sin trámite') AS nombre_servicio,
  COALESCE(cat.nombre, 'Sin categoría') AS categoria,
  COUNT(e.id)::integer AS total_tramites,
  COUNT(*) FILTER (WHERE e.estado_tramite = 'pendiente')::integer AS pendientes,
  COUNT(*) FILTER (WHERE e.estado_tramite = 'procesando')::integer AS procesando,
  COUNT(*) FILTER (WHERE e.estado_tramite = 'completada')::integer AS completadas,
  COUNT(*) FILTER (WHERE e.estado_tramite = 'cancelada')::integer AS canceladas,
  COALESCE(SUM(CASE WHEN e.estado_tramite = 'completada' THEN e.valor ELSE 0 END), 0)::numeric AS ingresos,
  COALESCE(SUM(CASE WHEN e.estado_tramite = 'completada' THEN COALESCE(vp.costo, tc.costo_base, 0) ELSE 0 END), 0)::numeric AS costo_estimado,
  COALESCE(SUM(CASE WHEN e.estado_tramite = 'completada' THEN e.valor - COALESCE(vp.costo, tc.costo_base, 0) ELSE 0 END), 0)::numeric AS utilidad_estimada,
  CASE
    WHEN COALESCE(SUM(CASE WHEN e.estado_tramite = 'completada' THEN e.valor ELSE 0 END), 0) > 0
      THEN ROUND(
        COALESCE(SUM(CASE WHEN e.estado_tramite = 'completada' THEN e.valor - COALESCE(vp.costo, tc.costo_base, 0) ELSE 0 END), 0)
        / COALESCE(SUM(CASE WHEN e.estado_tramite = 'completada' THEN e.valor ELSE 0 END), 0),
        4
      )
    ELSE NULL
  END AS margen
FROM public.entradas e
LEFT JOIN public.tramites_catalogo tc
  ON upper(trim(e.servicio)) = upper(trim(tc.nombre))
  OR upper(trim(e.servicio)) = upper(trim(tc.codigo))
LEFT JOIN public.tramites_categorias cat
  ON cat.id = tc.categoria_id
LEFT JOIN public.v_tramites_precios_vigentes vp
  ON vp.tramite_id = tc.id
GROUP BY
  COALESCE(tc.codigo, upper(trim(e.servicio))),
  COALESCE(tc.nombre, e.servicio, 'Sin trámite'),
  COALESCE(cat.nombre, 'Sin categoría')
ORDER BY ingresos DESC, total_tramites DESC;

