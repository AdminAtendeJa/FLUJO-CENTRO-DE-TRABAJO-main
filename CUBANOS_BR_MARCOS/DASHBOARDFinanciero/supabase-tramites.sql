-- 3) VISTA DE PRECIOS VIGENTES
-- ============================================================

DROP VIEW IF EXISTS public.v_tramites_precios_vigentes CASCADE;

CREATE OR REPLACE VIEW public.v_tramites_precios_vigentes
WITH (security_invoker = true) AS
SELECT
  ranked.precio_id,
  ranked.tramite_id,
  ranked.tramite_codigo,
  ranked.tramite_nombre,
  ranked.categoria_id,
  ranked.categoria_codigo,
  ranked.categoria_nombre,
  ranked.precio,
  ranked.costo,
  ranked.moneda,
  ranked.vigente_desde,
  ranked.vigente_hasta,
  ranked.activo,
  ranked.observaciones,
  ranked.creado_en,
  ranked.actualizado_en
FROM (
  SELECT
    tp.id AS precio_id,
    tp.tramite_id,
    tc.codigo AS tramite_codigo,
    tc.nombre AS tramite_nombre,
    tc.categoria_id,
    cat.codigo AS categoria_codigo,
    cat.nombre AS categoria_nombre,
    tp.precio,
    tp.costo,
    tp.moneda,
    tp.vigente_desde,
    tp.vigente_hasta,
    tp.activo,
    tp.observaciones,
    tp.creado_en,
    tp.actualizado_en,
    ROW_NUMBER() OVER (
      PARTITION BY tp.tramite_id
      ORDER BY tp.vigente_desde DESC, tp.id DESC
    ) AS rn
  FROM public.tramites_precios tp
  JOIN public.tramites_catalogo tc
    ON tc.id = tp.tramite_id
  LEFT JOIN public.tramites_categorias cat
    ON cat.id = tc.categoria_id
  WHERE tp.activo = true
    AND tp.vigente_desde <= CURRENT_DATE
    AND (tp.vigente_hasta IS NULL OR tp.vigente_hasta >= CURRENT_DATE)
) ranked
WHERE ranked.rn = 1;

-- ============================================================
-- 4) VISTA DETALLADA DE TRÁMITES
--    Une entradas + clientes + catálogo + precio vigente
-- ============================================================

DROP VIEW IF EXISTS public.v_tramites_detallado CASCADE;

CREATE OR REPLACE VIEW public.v_tramites_detallado
WITH (security_invoker = true) AS
SELECT
  e.*,
  c.nombre AS nombre_cliente,
  c.telefono AS telefono_cliente,
  c.email AS email_cliente,
  c.cpf AS cpf_cliente,
  c.pais AS pais_cliente,
  c.ciudad AS ciudad_cliente,
  c.estado_cliente,
  c.canal_adquisicion,
  c.atendente AS atendente_cliente,
  tc.id AS tramite_catalogo_id,
  tc.codigo AS tramite_codigo,
  tc.nombre AS tramite_nombre,
  cat.codigo AS categoria_codigo,
  cat.nombre AS categoria_nombre,
  COALESCE(vp.precio, tc.precio_base, e.valor, 0)::numeric(12,2) AS precio_catalogo,
  COALESCE(vp.costo, tc.costo_base, 0)::numeric(12,2) AS costo_catalogo,
  (COALESCE(vp.precio, tc.precio_base, e.valor, 0) - COALESCE(vp.costo, tc.costo_base, 0))::numeric(12,2) AS utilidad_estimada,
  CASE
    WHEN COALESCE(vp.precio, tc.precio_base, e.valor, 0) > 0
      THEN ROUND(
        (COALESCE(vp.precio, tc.precio_base, e.valor, 0) - COALESCE(vp.costo, tc.costo_base, 0))
        / COALESCE(vp.precio, tc.precio_base, e.valor, 0),
        4
      )
    ELSE NULL
  END AS margen_estimado
FROM public.entradas e
LEFT JOIN public.clientes c
  ON c.id = e.id_cliente
LEFT JOIN public.tramites_catalogo tc
  ON upper(trim(e.servicio)) = upper(trim(tc.nombre))
  OR upper(trim(e.servicio)) = upper(trim(tc.codigo))
LEFT JOIN public.tramites_categorias cat
  ON cat.id = tc.categoria_id
LEFT JOIN public.v_tramites_precios_vigentes vp
  ON vp.tramite_id = tc.id;

-- ============================================================
-- 5) VISTA POR CLIENTE
--    Totales y último trámite por cliente
-- ============================================================

DROP VIEW IF EXISTS public.v_clientes_tramites CASCADE;

CREATE OR REPLACE VIEW public.v_clientes_tramites
WITH (security_invoker = true) AS
SELECT
  c.*,
  COUNT(e.id)::integer AS total_tramites,
  COUNT(*) FILTER (WHERE e.estado_tramite = 'pendiente')::integer AS tramites_pendientes,
  COUNT(*) FILTER (WHERE e.estado_tramite = 'procesando')::integer AS tramites_procesando,
  COUNT(*) FILTER (WHERE e.estado_tramite = 'completada')::integer AS tramites_completados,
  COUNT(*) FILTER (WHERE e.estado_tramite = 'cancelada')::integer AS tramites_cancelados,
  COALESCE(SUM(CASE WHEN e.estado_tramite = 'completada' THEN e.valor ELSE 0 END), 0)::numeric AS valor_completado,
  COALESCE(SUM(CASE WHEN e.estado_tramite IN ('pendiente', 'procesando') THEN e.valor ELSE 0 END), 0)::numeric AS valor_pipeline,
  MAX(COALESCE(e.fecha_completacion, e.creado_en)) AS ultima_fecha_tramite,
  MAX(e.creado_en) AS fecha_ultima_entrada
FROM public.clientes c
LEFT JOIN public.entradas e
  ON e.id_cliente = c.id
GROUP BY c.id;

-- ============================================================
-- 6) RESUMEN MENSUAL
--    Trámites, clientes nuevos y salidas
-- ============================================================

DROP VIEW IF EXISTS public.v_resumen_mensual_tramites CASCADE;

CREATE OR REPLACE VIEW public.v_resumen_mensual_tramites
WITH (security_invoker = true) AS
WITH entradas_mes AS (
  SELECT
    date_trunc('month', COALESCE(e.fecha_completacion, e.creado_en)) AS mes,
    COUNT(*)::integer AS total_tramites,
    COUNT(*) FILTER (WHERE e.estado_tramite = 'pendiente')::integer AS pendientes,
    COUNT(*) FILTER (WHERE e.estado_tramite = 'procesando')::integer AS procesando,
    COUNT(*) FILTER (WHERE e.estado_tramite = 'completada')::integer AS completadas,
    COUNT(*) FILTER (WHERE e.estado_tramite = 'cancelada')::integer AS canceladas,
    COALESCE(SUM(CASE WHEN e.estado_tramite = 'completada' THEN e.valor ELSE 0 END), 0)::numeric AS ingresos,
    COALESCE(SUM(CASE WHEN e.estado_tramite IN ('pendiente', 'procesando') THEN e.valor ELSE 0 END), 0)::numeric AS pipeline,
    COALESCE(AVG(CASE WHEN e.estado_tramite = 'completada' THEN e.valor END), 0)::numeric AS ticket_promedio,
    COUNT(DISTINCT e.id_cliente)::integer AS clientes_activos
  FROM public.entradas e
  WHERE COALESCE(e.fecha_completacion, e.creado_en) IS NOT NULL
  GROUP BY 1
),
clientes_mes AS (
  SELECT
    date_trunc('month', c.creado_en) AS mes,
    COUNT(*)::integer AS clientes_nuevos
  FROM public.clientes c
  WHERE c.creado_en IS NOT NULL
  GROUP BY 1
),
salidas_mes AS (
  SELECT
    date_trunc('month', COALESCE(s.fecha, s.creado_en)) AS mes,
    COALESCE(SUM(s.valor), 0)::numeric AS egresos
  FROM public.salidas s
  WHERE COALESCE(s.fecha, s.creado_en) IS NOT NULL
  GROUP BY 1
),
meses AS (
  SELECT mes FROM entradas_mes
  UNION
  SELECT mes FROM clientes_mes
  UNION
  SELECT mes FROM salidas_mes
)
SELECT
  m.mes,
  COALESCE(em.total_tramites, 0) AS total_tramites,
  COALESCE(em.pendientes, 0) AS pendientes,
  COALESCE(em.procesando, 0) AS procesando,
  COALESCE(em.completadas, 0) AS completadas,
  COALESCE(em.canceladas, 0) AS canceladas,
  COALESCE(em.ingresos, 0) AS ingresos,
  COALESCE(sm.egresos, 0) AS egresos,
  COALESCE(em.ingresos, 0) - COALESCE(sm.egresos, 0) AS utilidad_neta,
  COALESCE(em.pipeline, 0) AS pipeline,
  COALESCE(em.ticket_promedio, 0) AS ticket_promedio,
  COALESCE(em.clientes_activos, 0) AS clientes_activos,
  COALESCE(cm.clientes_nuevos, 0) AS clientes_nuevos
FROM meses m
LEFT JOIN entradas_mes em
  ON em.mes = m.mes
LEFT JOIN clientes_mes cm
  ON cm.mes = m.mes
LEFT JOIN salidas_mes sm
  ON sm.mes = m.mes
ORDER BY m.mes DESC;

-- ============================================================
-- 7) VISTA PRINCIPAL DEL DASHBOARD
--    KPIs del mes actual en una sola fila
-- ============================================================

DROP VIEW IF EXISTS public.v_dashboard_principal CASCADE;

CREATE OR REPLACE VIEW public.v_dashboard_principal
WITH (security_invoker = true) AS
WITH resumen AS (
  SELECT *
  FROM public.v_resumen_mensual_tramites
),
mes_actual AS (
  SELECT *
  FROM resumen
  WHERE mes = date_trunc('month', CURRENT_DATE::timestamptz)
  LIMIT 1
),
ultimo_mes AS (
  SELECT *
  FROM resumen
  ORDER BY mes DESC
  LIMIT 1
)
SELECT
  COALESCE(ma.mes, lm.mes, date_trunc('month', CURRENT_DATE::timestamptz)) AS mes,
  COALESCE(ma.total_tramites, 0) AS total_tramites,
  COALESCE(ma.completadas, 0) AS completadas,
  COALESCE(ma.pendientes, 0) AS pendientes,
  COALESCE(ma.procesando, 0) AS procesando,
  COALESCE(ma.canceladas, 0) AS canceladas,
  COALESCE(ma.ingresos, 0) AS ingresos,
  COALESCE(ma.egresos, 0) AS egresos,
  COALESCE(ma.utilidad_neta, 0) AS utilidad_neta,
  COALESCE(ma.pipeline, 0) AS pipeline,
  COALESCE(ma.ticket_promedio, 0) AS ticket_promedio,
  COALESCE(ma.clientes_activos, 0) AS clientes_activos,
  COALESCE(ma.clientes_nuevos, 0) AS clientes_nuevos,
  COALESCE(lm.total_tramites, 0) AS total_tramites_mes_anterior,
  COALESCE(lm.ingresos, 0) AS ingresos_mes_anterior,
  COALESCE(lm.utilidad_neta, 0) AS utilidad_neta_mes_anterior
FROM (SELECT 1) base
LEFT JOIN mes_actual ma
  ON true
LEFT JOIN ultimo_mes lm
  ON true;

