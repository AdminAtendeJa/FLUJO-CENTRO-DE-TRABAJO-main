CREATE OR REPLACE FUNCTION public.delete_entrada_cascade(p_entrada_id bigint)
RETURNS boolean
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  deleted_rows integer;
BEGIN
  DELETE FROM public.historial_cambios
  WHERE id_entrada = p_entrada_id;

  DELETE FROM public.entradas
  WHERE id = p_entrada_id;

  GET DIAGNOSTICS deleted_rows = ROW_COUNT;
  RETURN deleted_rows > 0;
END;
$$;

GRANT EXECUTE ON FUNCTION public.delete_entrada_cascade(bigint) TO authenticated;

